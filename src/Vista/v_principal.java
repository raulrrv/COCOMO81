    /**
     * Controla qué fórmulas aplicar de acuerdo al Tipo de Proyecto
     * seleccionado.
     *
     * @param evt
     * @since COCOMO1 1.0.0
     */
    private void btnContinuarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnContinuarActionPerformed
        if (validarTotalLDC(objM_KLOC.getLDC())) {  //comprueba si el total de LDC es mayor a 1000

            if (objM_KLOC.getLDC() <= 50000) {         //si las lineas de codigo son menores a 50000 se trata de un proyecto orgánico
                cmbTipos.setSelectedItem("Orgánico");
            } else if (objM_KLOC.getLDC() <= 300000) { //si las lineas de codigo son menores a 300000 se trata de un proyecto semi-separado
                cmbTipos.setSelectedItem("Semi-separado");
            } else {                //si las lineas de codigo son mayores a 300000 se trata de un proyecto incrustado
                cmbTipos.setSelectedItem("Incrustado");
            }
            this.panelInicio.setVisible(false);
            this.panelResultado.setVisible(true);
            lblResultLDC.setText(String.valueOf(objM_KLOC.getLDC()));     //mostrar LDC final en panel resultados
            Calcular();
            lblResultKLOC.setText(String.valueOf(String.format("%.2f", objM_KLOC.getKLOC())));   //mostrar KLOC final en panel resultados

        } else {
            JOptionPane.showMessageDialog(null, "El número de LDC es menor a 1000, pruebe agregando más archivos.", "No fue posible aplicar COCOMO", JOptionPane.ERROR_MESSAGE);
        }


    }//GEN-LAST:event_btnContinuarActionPerformed
    /**
     * Comprueba si el total de Líneas de Código (LDC) es mayor a 1000.
     *
     * @param LDC
     * @return Valor booleano
     * @since COCOMO1 1.0.0
     */
    public static boolean validarTotalLDC(int LDC) {  //comprueba si el total de LDC es mayor a 1000
        boolean bool = false;
        if (LDC >= 1000) {
            bool = true;
        } else {
            bool = false;
        }
        return bool;
    }

    /**
     * Controla que el archivo seleccionado sea válido, solicita llenar la
     * tabla, habilita el botón "Continuar" y deshabilita el botón "Obtenet
     * LDC".
     *
     * @param evt
     * @since COCOMO1 1.0.0
     */
    private void btnObtenerLDCActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnObtenerLDCActionPerformed

        c_archivo objC_Archivo = new c_archivo();

        try {
            if (archivo.canRead()) {            //comprobar si se seleccionó un archivo

                if (objC_Archivo.formato(archivo)) {    //comprobar si es un formato de archivo soportado

                    objC_Archivo.leer(archivo);     //lee y cuenta las lineas de código

                    llenarTabla(objC_Archivo.getNumCodigo(), objC_Archivo.getNumComent(), objC_Archivo.getNumBlanco());

                    habilitarBotonContinuar();
                    deshabilitarBotonObtenerLDC();
                }

            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Archivo ilegible. \nError: " + e);
        }


    }//GEN-LAST:event_btnObtenerLDCActionPerformed
