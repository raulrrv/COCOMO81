    /**
     * Método encargado de hacer los cálculos para determinar COCOMO Básico de
     * acuerdo a las fórmulas del mismo
     *
     * @since COCOMO1 1.0.0
     */
    public void Calcular() {
        /*
            E = ab(KLOC)bb
            Tdev = cb(e)db
            N = E/Tdev
            Pr = LDC/E
            
         */
        double Ab, Bb, Cb, Db;
        int LDC = objM_KLOC.getLDC();
        //y lo transforma a kilo lineas de codigo, dividiendo para 1000
        //System.out.println("valor LDC: " + objM_KLOC.getLDC());
        double KLOC = (double) objM_KLOC.getLDC() / 1000;
        //  System.out.println(KLOC + "       ");
        objM_KLOC.setKLOC(KLOC);                  //asignat al objeto el valor de KLOC

        String strTipoProyecto = (String) cmbTipos.getSelectedItem();       //obtiene el valor del item seleccionando
        switch (strTipoProyecto) {
            case "Orgánico":
                //asignar valores de acuerdo al tipo de proyecto
                Ab = 2.4;
                Bb = 1.05;
                Cb = 2.5;
                Db = 0.38;

                //  calcular ESFUERZO por el proyecto, en persona-mes   Esfuerzo = Ab * (KLOC) ^ Bb
                double E = Ab * Math.pow(KLOC, Bb);
                lblResultEsfuerzo.setText(Math.round(E) + " personas-mes");           //mostrar valor del esfuerzo

                //  calcular tiempo requerido por el proyecto en meses  Tiempo Requerido = Cb * (esfuerzos) Db
                double Tdev = Cb * Math.pow(E, Db);
                lblResultTiempo.setText(Math.round(Tdev) + " meses");           //mostrar valor del tiempo requerido

                //  calcular número de personas requerido por el proyecto
                double N = E / Tdev;
                lblResultPersonas.setText(Math.round(N) + " personas");           //mostrar el número de personas requeridas

                //  calcular la productividad
                double Pr = LDC / E;
                lblResultProductividad.setText(String.format("%.2f", Pr) + "");           //mostrar productividad, redondeando a 2 decimales
                break;
            case "Semi-separado":
                //asignar valores de acuerdo al tipo de proyecto
                Ab = 3;
                Bb = 1.12;
                Cb = 2.5;
                Db = 0.35;
                //  calcular ESFUERZO por el proyecto, en persona-mes   Esfuerzo = Ab * (KLOC) ^ Bb
                //Match.pow usado para elevar a una potencia
                E = Ab * Math.pow(KLOC, Bb);
                //mostrar valor del esfuerzo
                //Match.round usado para redondear
                lblResultEsfuerzo.setText(Math.round(E) + " personas-mes");

                //  calcular tiempo requerido por el proyecto en meses  Tiempo Requerido = Cb * (esfuerzos) Db
                Tdev = Cb * Math.pow(E, Db);
                lblResultTiempo.setText(Math.round(Tdev) + " meses");           //mostrar valor del tiempo requerido

                //  calcular número de personas requerido por el proyecto
                N = E / Tdev;
                lblResultPersonas.setText(Math.round(N) + " personas");           //mostrar el número de personas requeridas

                //  calcular la productividad
                Pr = LDC / E;
                lblResultProductividad.setText(String.format("%.2f", Pr) + "");           //mostrar productividad, redondeando a 2 decimales
                break;
            case "Incrustado":
                //asignar valores de acuerdo al tipo de proyecto
                Ab = 3.6;
                Bb = 1.2;
                Cb = 2.5;
                Db = 0.32;
                //  calcular ESFUERZO por el proyecto, en persona-mes   Esfuerzo = Ab * (KLOC) ^ Bb
                //Match.pow usado para elevar a una potencia
                E = Ab * Math.pow(KLOC, Bb);
                //mostrar valor del esfuerzo
                //Match.round usado para redondear
                lblResultEsfuerzo.setText(Math.round(E) + " personas-mes");

                //  calcular tiempo requerido por el proyecto en meses  Tiempo Requerido = Cb * (esfuerzos) Db
                Tdev = Cb * Math.pow(E, Db);
                lblResultTiempo.setText(Math.round(Tdev) + " meses");           //mostrar valor del tiempo requerido

                //  calcular número de personas requerido por el proyecto
                N = E / Tdev;
                lblResultPersonas.setText(Math.round(N) + " personas");           //mostrar el número de personas requeridas

                //  calcular la productividad
                Pr = LDC / E;
                lblResultProductividad.setText(String.format("%.2f", Pr) + "");           //mostrar productividad, redondeando a 2 decimales
                break;

            default:
                break;
        }
    }

    /**
     * Inserta las filas a la tabla de acuerdo a cada archivo leído.
     *
     * @param numCodigo
     * @param numComent
     * @param numBlanco
     * @since COCOMO1 1.0.0
     */
    public void llenarTabla(int numCodigo, int numComent, int numBlanco) {

        tablaModelo = (DefaultTableModel) tblResumen.getModel();
        String fila[] = new String[4];
        contFila++;
        fila[0] = String.valueOf(contFila);
        fila[1] = String.valueOf(numCodigo);
        fila[2] = String.valueOf(numComent);
        fila[3] = String.valueOf(numBlanco);
        tablaModelo.addRow(fila);
        //System.out.println("total de lineas: \nblanco: " + numBlanco + " codigo: " + numCodigo + " coment: " + numComent);

        c_KLOC objC_KLOC = new c_KLOC();
        objC_KLOC.SumatoriaLDC(numCodigo);  //sumatoria de LDC de cada archivo

        lblLDC.setText(String.valueOf(objM_KLOC.getLDC())); //mostrar sumatoria total de LDC
    }

